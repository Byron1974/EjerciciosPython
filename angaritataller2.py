# Función que recibe un nombre y retorna el número de letras de ese nombre
def contar_letras(nombre):
    return len(nombre)

# Llamar a la función con un nombre y guardar el valor de retorno
numero_de_letras = contar_letras("Byron Daniel Penaloza Pena")

# Imprimir el valor retornado por la función
print(f"El número de letras en el nombre es: {numero_de_letras}")
